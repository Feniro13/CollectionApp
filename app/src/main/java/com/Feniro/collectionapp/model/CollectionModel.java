package com.Feniro.collectionapp.model;

public class CollectionModel {

    int id;
    String name;

    public CollectionModel(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
